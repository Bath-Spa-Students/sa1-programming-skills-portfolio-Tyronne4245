# First Step is printing a label for the kind of Loop 
print("\nCounting up from 0 to 50 in increments of 1:")

# Next Step is creating a for loop that starts with 0, ends before 51, with an increment of 1
for x in range(0, 51, 1):
    print(x)

# The other instructions will follow the previous ones with different starts,ends,and increments

# Loop that counts down from 50 to 0 in decrements of 1
print("\nCounting down from 50 to 0 in decrements of 1:")
for x in range(50, -1, -1):
    print(x)

# Loop that counts up from 30 to 50 in increments of 1
print("\nCounting up from 30 to 50 in increments of 1:")
for x in range(30, 51, 1):
    print(x)

